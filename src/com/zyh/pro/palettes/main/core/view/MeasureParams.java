package com.zyh.pro.palettes.main.core.view;

public class MeasureParams {

	private final int specParam;

	private final int margin;

	public MeasureParams(int specParam, int margin) {
		this.specParam = specParam;
		this.margin = margin;
	}

	public int getSpecParam() {
		return specParam;
	}

	public int getMargin() {
		return margin;
	}
}
